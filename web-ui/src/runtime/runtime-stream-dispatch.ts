/**
 * Pure message-to-action resolution for incoming RuntimeStateStream messages.
 *
 * This module intentionally does not own any WebSocket side effects. It takes
 * the current active project id, resolves which reducer actions should run,
 * and reports whether the transport should reconnect to a different project.
 */
import type { RuntimeStateStreamAction } from "@/runtime/runtime-state-stream-store";
import type { RuntimeStateStreamMessage, RuntimeStateStreamProjectsMessage } from "@/runtime/types";

export interface RuntimeStreamDispatchState {
	activeProjectId: string | null;
}

export interface RuntimeStreamDispatchResult {
	actions: RuntimeStateStreamAction[];
	nextActiveProjectId: string | null;
	reconnectProjectId: string | null;
}

type StreamMessageHandler<T extends RuntimeStateStreamMessage["type"]> = (
	message: Extract<RuntimeStateStreamMessage, { type: T }>,
	state: RuntimeStreamDispatchState,
) => RuntimeStreamDispatchResult;

function createDispatchResult(
	actions: RuntimeStateStreamAction[],
	nextActiveProjectId: string | null,
	reconnectProjectId: string | null = null,
): RuntimeStreamDispatchResult {
	return {
		actions,
		nextActiveProjectId,
		reconnectProjectId,
	};
}

export function resolveProjectIdAfterProjectsUpdate(
	currentProjectId: string | null,
	payload: RuntimeStateStreamProjectsMessage,
): string | null {
	if (currentProjectId && payload.projects.some((project) => project.id === currentProjectId)) {
		return currentProjectId;
	}
	return payload.currentProjectId;
}

const streamMessageHandlers: {
	[K in RuntimeStateStreamMessage["type"]]: StreamMessageHandler<K>;
} = {
	snapshot: (msg) => createDispatchResult([{ type: "snapshot", payload: msg }], msg.currentProjectId),

	projects_updated: (msg, state) => {
		const nextProjectId = resolveProjectIdAfterProjectsUpdate(state.activeProjectId, msg);
		const reconnectProjectId = nextProjectId && nextProjectId !== state.activeProjectId ? nextProjectId : null;
		return createDispatchResult(
			[{ type: "projects_updated", payload: msg, nextProjectId }],
			nextProjectId,
			reconnectProjectId,
		);
	},

	project_state_updated: (msg, state) =>
		msg.projectId !== state.activeProjectId
			? createDispatchResult([], state.activeProjectId)
			: createDispatchResult(
					[{ type: "project_state_updated", projectState: msg.projectState }],
					state.activeProjectId,
				),

	project_metadata_updated: (msg, state) =>
		msg.projectId !== state.activeProjectId
			? createDispatchResult([], state.activeProjectId)
			: createDispatchResult(
					[{ type: "project_metadata_updated", projectMetadata: msg.projectMetadata }],
					state.activeProjectId,
				),

	task_sessions_updated: (msg, state) =>
		msg.projectId !== state.activeProjectId
			? createDispatchResult([], state.activeProjectId)
			: createDispatchResult([{ type: "task_sessions_updated", summaries: msg.summaries }], state.activeProjectId),

	task_notification: (msg, state) =>
		createDispatchResult(
			[{ type: "task_notification", projectId: msg.projectId, summaries: msg.summaries }],
			state.activeProjectId,
		),

	task_ready_for_review: (msg, state) =>
		msg.projectId !== state.activeProjectId
			? createDispatchResult([], state.activeProjectId)
			: createDispatchResult([{ type: "task_ready_for_review", payload: msg }], state.activeProjectId),

	task_title_updated: (msg, state) =>
		msg.projectId !== state.activeProjectId
			? createDispatchResult([], state.activeProjectId)
			: createDispatchResult(
					[
						{
							type: "task_title_updated",
							taskId: msg.taskId,
							title: msg.title,
							autoGenerated: msg.autoGenerated,
						},
					],
					state.activeProjectId,
				),

	task_base_ref_updated: (msg, state) =>
		msg.projectId !== state.activeProjectId
			? createDispatchResult([], state.activeProjectId)
			: createDispatchResult(
					[
						{
							type: "task_base_ref_updated",
							taskId: msg.taskId,
							baseRef: msg.baseRef,
						},
					],
					state.activeProjectId,
				),

	debug_logging_state: (msg, state) =>
		createDispatchResult(
			[{ type: "debug_logging_state", level: msg.level, recentEntries: msg.recentEntries }],
			state.activeProjectId,
		),

	debug_log_batch: (msg, state) =>
		createDispatchResult([{ type: "debug_log_batch", entries: msg.entries }], state.activeProjectId),

	error: (msg, state) => createDispatchResult([{ type: "stream_error", message: msg.message }], state.activeProjectId),
};

export function resolveStreamMessage(
	payload: RuntimeStateStreamMessage,
	state: RuntimeStreamDispatchState,
): RuntimeStreamDispatchResult {
	const handler = streamMessageHandlers[payload.type] as (
		msg: RuntimeStateStreamMessage,
		state: RuntimeStreamDispatchState,
	) => RuntimeStreamDispatchResult;
	return handler(payload, state);
}

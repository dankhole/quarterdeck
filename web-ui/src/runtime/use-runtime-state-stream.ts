import { useEffect, useReducer } from "react";
import { consumeProjectPreload } from "@/runtime/project-preload-cache";
import { setRuntimeDisconnected } from "@/runtime/runtime-connection-state";
import type {
	RuntimeDebugLogEntry,
	RuntimeProjectSummary,
	RuntimeStateStreamMessage,
	RuntimeStateStreamProjectsMessage,
	RuntimeStateStreamSnapshotMessage,
	RuntimeStateStreamTaskReadyForReviewMessage,
	RuntimeTaskSessionSummary,
	RuntimeWorkspaceMetadata,
	RuntimeWorkspaceStateResponse,
} from "@/runtime/types";
import { createClientLogger } from "@/utils/client-logger";
import { toErrorMessage } from "@/utils/to-error-message";

const log = createClientLogger("ws-stream");

const STREAM_RECONNECT_BASE_DELAY_MS = 500;
const STREAM_RECONNECT_MAX_DELAY_MS = 5_000;
const DEBUG_LOG_ENTRIES_MAX = 500;

function mergeTaskSessionSummaries(
	currentSessions: Record<string, RuntimeTaskSessionSummary>,
	summaries: RuntimeTaskSessionSummary[],
): Record<string, RuntimeTaskSessionSummary> {
	if (summaries.length === 0) {
		return currentSessions;
	}
	const nextSessions = { ...currentSessions };
	for (const summary of summaries) {
		const existing = nextSessions[summary.taskId];
		if (!existing || existing.updatedAt <= summary.updatedAt) {
			nextSessions[summary.taskId] = summary;
		}
	}
	return nextSessions;
}

function getRuntimeStreamUrl(workspaceId: string | null): string {
	const protocol = window.location.protocol === "https:" ? "wss:" : "ws:";
	const url = new URL(`${protocol}//${window.location.host}/api/runtime/ws`);
	if (workspaceId) {
		url.searchParams.set("workspaceId", workspaceId);
	}
	return url.toString();
}

export interface TaskTitleUpdate {
	taskId: string;
	title: string;
	autoGenerated?: boolean;
}

export interface UseRuntimeStateStreamResult {
	currentProjectId: string | null;
	projects: RuntimeProjectSummary[];
	workspaceState: RuntimeWorkspaceStateResponse | null;
	workspaceMetadata: RuntimeWorkspaceMetadata | null;
	notificationSessions: Record<string, RuntimeTaskSessionSummary>;
	notificationWorkspaceIds: Record<string, string>;
	latestTaskReadyForReview: RuntimeStateStreamTaskReadyForReviewMessage | null;
	latestTaskTitleUpdate: TaskTitleUpdate | null;
	debugLoggingEnabled: boolean;
	debugLogEntries: RuntimeDebugLogEntry[];
	streamError: string | null;
	isRuntimeDisconnected: boolean;
	hasReceivedSnapshot: boolean;
}

interface RuntimeStateStreamStore {
	currentProjectId: string | null;
	projects: RuntimeProjectSummary[];
	workspaceState: RuntimeWorkspaceStateResponse | null;
	workspaceMetadata: RuntimeWorkspaceMetadata | null;
	notificationSessions: Record<string, RuntimeTaskSessionSummary>;
	notificationWorkspaceIds: Record<string, string>;
	latestTaskReadyForReview: RuntimeStateStreamTaskReadyForReviewMessage | null;
	latestTaskTitleUpdate: TaskTitleUpdate | null;
	debugLoggingEnabled: boolean;
	debugLogEntries: RuntimeDebugLogEntry[];
	streamError: string | null;
	isRuntimeDisconnected: boolean;
	hasReceivedSnapshot: boolean;
}

type RuntimeStateStreamAction =
	| {
			type: "requested_workspace_changed";
			preloadedWorkspaceState: RuntimeWorkspaceStateResponse | null;
			requestedWorkspaceId: string | null;
	  }
	| { type: "stream_connected" }
	| { type: "snapshot"; payload: RuntimeStateStreamSnapshotMessage }
	| {
			type: "projects_updated";
			payload: RuntimeStateStreamProjectsMessage;
			nextProjectId: string | null;
	  }
	| { type: "workspace_metadata_updated"; workspaceMetadata: RuntimeWorkspaceMetadata }
	| { type: "task_ready_for_review"; payload: RuntimeStateStreamTaskReadyForReviewMessage }
	| { type: "task_title_updated"; taskId: string; title: string; autoGenerated?: boolean }
	| { type: "workspace_state_updated"; workspaceState: RuntimeWorkspaceStateResponse }
	| { type: "task_sessions_updated"; summaries: RuntimeTaskSessionSummary[] }
	| { type: "task_notification"; workspaceId: string; summaries: RuntimeTaskSessionSummary[] }
	| { type: "debug_logging_state"; enabled: boolean; recentEntries?: RuntimeDebugLogEntry[] }
	| { type: "debug_log_batch"; entries: RuntimeDebugLogEntry[] }
	| { type: "debug_log_clear" }
	| { type: "stream_error"; message: string }
	| { type: "stream_disconnected"; message: string };

function createInitialRuntimeStateStreamStore(requestedWorkspaceId: string | null): RuntimeStateStreamStore {
	return {
		currentProjectId: requestedWorkspaceId,
		projects: [],
		workspaceState: null,
		workspaceMetadata: null,
		notificationSessions: {},
		notificationWorkspaceIds: {},
		latestTaskReadyForReview: null,
		latestTaskTitleUpdate: null,
		debugLoggingEnabled: false,
		debugLogEntries: [],
		streamError: null,
		isRuntimeDisconnected: false,
		hasReceivedSnapshot: false,
	};
}

function resolveProjectIdAfterProjectsUpdate(
	currentProjectId: string | null,
	payload: RuntimeStateStreamProjectsMessage,
): string | null {
	if (currentProjectId && payload.projects.some((project) => project.id === currentProjectId)) {
		return currentProjectId;
	}
	return payload.currentProjectId;
}

function runtimeStateStreamReducer(
	state: RuntimeStateStreamStore,
	action: RuntimeStateStreamAction,
): RuntimeStateStreamStore {
	if (action.type === "requested_workspace_changed") {
		if (action.preloadedWorkspaceState) {
			// Advance currentProjectId immediately — we have preloaded data to render
			// without waiting for the WebSocket snapshot. The non-preloaded path leaves
			// currentProjectId unchanged; the snapshot action updates it.
			const preloadedSessions = Object.values(action.preloadedWorkspaceState.sessions ?? {});
			return {
				...state,
				currentProjectId: action.requestedWorkspaceId,
				workspaceState: action.preloadedWorkspaceState,
				workspaceMetadata: null,
				notificationSessions: mergeTaskSessionSummaries(state.notificationSessions, preloadedSessions),
				notificationWorkspaceIds: action.requestedWorkspaceId
					? {
							...state.notificationWorkspaceIds,
							...Object.fromEntries(preloadedSessions.map((s) => [s.taskId, action.requestedWorkspaceId!])),
						}
					: state.notificationWorkspaceIds,
				streamError: null,
				isRuntimeDisconnected: false,
				hasReceivedSnapshot: true,
			};
		}
		return {
			...state,
			workspaceState: null,
			workspaceMetadata: null,
			streamError: null,
			isRuntimeDisconnected: false,
			hasReceivedSnapshot: false,
		};
	}
	if (action.type === "stream_connected") {
		return {
			...state,
			streamError: null,
			isRuntimeDisconnected: false,
		};
	}
	if (action.type === "snapshot") {
		const snapshotSessions = action.payload.workspaceState?.sessions ?? {};
		const nextWorkspaceState = action.payload.workspaceState
			? {
					...action.payload.workspaceState,
					sessions: mergeTaskSessionSummaries(
						state.workspaceState?.sessions ?? {},
						Object.values(snapshotSessions),
					),
				}
			: null;
		return {
			currentProjectId: action.payload.currentProjectId,
			projects: action.payload.projects,
			workspaceState: nextWorkspaceState,
			workspaceMetadata: action.payload.workspaceMetadata,
			// Seeded from the active workspace's sessions only; other workspaces'
			// sessions arrive incrementally via `task_notification` messages.
			notificationSessions: mergeTaskSessionSummaries(state.notificationSessions, Object.values(snapshotSessions)),
			notificationWorkspaceIds: action.payload.currentProjectId
				? {
						...state.notificationWorkspaceIds,
						...Object.fromEntries(
							Object.keys(snapshotSessions).map((taskId) => [taskId, action.payload.currentProjectId!]),
						),
					}
				: state.notificationWorkspaceIds,
			latestTaskReadyForReview: state.latestTaskReadyForReview,
			latestTaskTitleUpdate: state.latestTaskTitleUpdate,
			debugLoggingEnabled: state.debugLoggingEnabled,
			debugLogEntries: state.debugLogEntries,
			streamError: null,
			isRuntimeDisconnected: false,
			hasReceivedSnapshot: true,
		};
	}
	if (action.type === "projects_updated") {
		const didProjectChange = action.nextProjectId !== state.currentProjectId;
		return {
			...state,
			currentProjectId: action.nextProjectId,
			projects: action.payload.projects,
			workspaceState: didProjectChange ? null : state.workspaceState,
			workspaceMetadata: didProjectChange ? null : state.workspaceMetadata,
			latestTaskReadyForReview: didProjectChange ? null : state.latestTaskReadyForReview,
			hasReceivedSnapshot: true,
		};
	}
	if (action.type === "workspace_metadata_updated") {
		return {
			...state,
			workspaceMetadata: action.workspaceMetadata,
		};
	}
	if (action.type === "task_ready_for_review") {
		return {
			...state,
			latestTaskReadyForReview: action.payload,
		};
	}
	if (action.type === "task_title_updated") {
		return {
			...state,
			latestTaskTitleUpdate: { taskId: action.taskId, title: action.title, autoGenerated: action.autoGenerated },
		};
	}
	if (action.type === "workspace_state_updated") {
		const mergedWorkspaceState = {
			...action.workspaceState,
			sessions: mergeTaskSessionSummaries(
				state.workspaceState?.sessions ?? {},
				Object.values(action.workspaceState.sessions ?? {}),
			),
		};
		return {
			...state,
			workspaceState: mergedWorkspaceState,
		};
	}
	if (action.type === "task_sessions_updated") {
		if (!state.workspaceState) {
			return state;
		}
		return {
			...state,
			workspaceState: {
				...state.workspaceState,
				sessions: mergeTaskSessionSummaries(state.workspaceState.sessions, action.summaries),
			},
		};
	}
	if (action.type === "task_notification") {
		// notificationSessions grows monotonically (entries are never removed). This is
		// intentional: the set stays small (one entry per task that emits a notification)
		// and useAudibleNotifications compares against the previous snapshot to detect
		// new events, so pruning would cause duplicate notifications on reconnect.
		return {
			...state,
			notificationSessions: mergeTaskSessionSummaries(state.notificationSessions, action.summaries),
			notificationWorkspaceIds: {
				...state.notificationWorkspaceIds,
				...Object.fromEntries(action.summaries.map((s) => [s.taskId, action.workspaceId])),
			},
		};
	}
	if (action.type === "debug_logging_state") {
		const incoming = action.recentEntries ?? [];
		// Merge recent entries from server, deduplicating by id.
		const existingIds = new Set(state.debugLogEntries.map((e) => e.id));
		const newEntries = incoming.filter((e) => !existingIds.has(e.id));
		const merged = [...state.debugLogEntries, ...newEntries].slice(-DEBUG_LOG_ENTRIES_MAX);
		return {
			...state,
			debugLoggingEnabled: action.enabled,
			debugLogEntries: action.enabled ? merged : state.debugLogEntries,
		};
	}
	if (action.type === "debug_log_batch") {
		const merged = [...state.debugLogEntries, ...action.entries].slice(-DEBUG_LOG_ENTRIES_MAX);
		return {
			...state,
			debugLogEntries: merged,
		};
	}
	if (action.type === "debug_log_clear") {
		return {
			...state,
			debugLogEntries: [],
		};
	}
	if (action.type === "stream_error") {
		return {
			...state,
			streamError: action.message,
			isRuntimeDisconnected: false,
		};
	}
	if (action.type === "stream_disconnected") {
		return {
			...state,
			streamError: action.message,
			isRuntimeDisconnected: true,
		};
	}
	return state;
}

export function useRuntimeStateStream(requestedWorkspaceId: string | null): UseRuntimeStateStreamResult {
	const [state, dispatch] = useReducer(
		runtimeStateStreamReducer,
		requestedWorkspaceId,
		createInitialRuntimeStateStreamStore,
	);
	useEffect(() => {
		let cancelled = false;
		let socket: WebSocket | null = null;
		let reconnectTimer: number | null = null;
		let reconnectAttempt = 0;
		let activeWorkspaceId = requestedWorkspaceId;
		let requestedWorkspaceForConnection = requestedWorkspaceId;

		const preloadedWorkspaceState = requestedWorkspaceId ? consumeProjectPreload(requestedWorkspaceId) : null;
		dispatch({ type: "requested_workspace_changed", preloadedWorkspaceState, requestedWorkspaceId });

		const cleanupSocket = () => {
			if (socket) {
				socket.onopen = null;
				socket.onmessage = null;
				socket.onerror = null;
				socket.onclose = null;
				socket.close();
				socket = null;
			}
		};

		const scheduleReconnect = () => {
			if (cancelled) {
				return;
			}
			if (reconnectTimer !== null) {
				return;
			}
			const delay = Math.min(STREAM_RECONNECT_MAX_DELAY_MS, STREAM_RECONNECT_BASE_DELAY_MS * 2 ** reconnectAttempt);
			reconnectAttempt += 1;
			reconnectTimer = window.setTimeout(() => {
				connect();
			}, delay);
		};

		const connect = () => {
			if (cancelled) {
				return;
			}
			if (reconnectTimer !== null) {
				window.clearTimeout(reconnectTimer);
				reconnectTimer = null;
			}
			cleanupSocket();
			try {
				socket = new WebSocket(getRuntimeStreamUrl(requestedWorkspaceForConnection));
			} catch (error) {
				setRuntimeDisconnected(true);
				dispatch({
					type: "stream_disconnected",
					message: toErrorMessage(error),
				});
				scheduleReconnect();
				return;
			}
			socket.onopen = () => {
				reconnectAttempt = 0;
				setRuntimeDisconnected(false);
				dispatch({ type: "stream_connected" });
			};
			socket.onmessage = (event) => {
				try {
					const payload = JSON.parse(String(event.data)) as RuntimeStateStreamMessage;
					if (payload.type === "snapshot") {
						activeWorkspaceId = payload.currentProjectId;
						dispatch({ type: "snapshot", payload });
						return;
					}
					if (payload.type === "projects_updated") {
						const previousWorkspaceId = activeWorkspaceId;
						const nextProjectId = resolveProjectIdAfterProjectsUpdate(activeWorkspaceId, payload);
						activeWorkspaceId = nextProjectId;
						dispatch({
							type: "projects_updated",
							payload,
							nextProjectId,
						});
						if (nextProjectId && nextProjectId !== previousWorkspaceId) {
							requestedWorkspaceForConnection = nextProjectId;
							dispatch({
								type: "requested_workspace_changed",
								preloadedWorkspaceState: null,
								requestedWorkspaceId: nextProjectId,
							});
							connect();
						}
						return;
					}
					if (payload.type === "workspace_state_updated") {
						if (payload.workspaceId !== activeWorkspaceId) {
							return;
						}
						dispatch({
							type: "workspace_state_updated",
							workspaceState: payload.workspaceState,
						});
						return;
					}
					if (payload.type === "workspace_metadata_updated") {
						if (payload.workspaceId !== activeWorkspaceId) {
							return;
						}
						dispatch({
							type: "workspace_metadata_updated",
							workspaceMetadata: payload.workspaceMetadata,
						});
						return;
					}
					if (payload.type === "task_sessions_updated") {
						if (payload.workspaceId !== activeWorkspaceId) {
							return;
						}
						dispatch({
							type: "task_sessions_updated",
							summaries: payload.summaries,
						});
						return;
					}
					if (payload.type === "task_notification") {
						dispatch({
							type: "task_notification",
							workspaceId: payload.workspaceId,
							summaries: payload.summaries,
						});
						return;
					}
					if (payload.type === "task_ready_for_review") {
						if (payload.workspaceId !== activeWorkspaceId) {
							return;
						}
						dispatch({
							type: "task_ready_for_review",
							payload,
						});
						return;
					}
					if (payload.type === "task_title_updated") {
						if (payload.workspaceId !== activeWorkspaceId) {
							return;
						}
						dispatch({
							type: "task_title_updated",
							taskId: payload.taskId,
							title: payload.title,
							autoGenerated: payload.autoGenerated,
						});
						return;
					}
					if (payload.type === "debug_logging_state") {
						dispatch({
							type: "debug_logging_state",
							enabled: payload.enabled,
							recentEntries: payload.recentEntries,
						});
						return;
					}
					if (payload.type === "debug_log_batch") {
						dispatch({
							type: "debug_log_batch",
							entries: payload.entries,
						});
						return;
					}
					if (payload.type === "error") {
						dispatch({
							type: "stream_error",
							message: payload.message,
						});
					}
				} catch (err) {
					log.warn("Malformed stream message", err);
				}
			};
			socket.onclose = () => {
				if (cancelled) {
					return;
				}
				setRuntimeDisconnected(true);
				dispatch({
					type: "stream_disconnected",
					message: "Runtime stream disconnected.",
				});
				scheduleReconnect();
			};
			socket.onerror = () => {
				if (cancelled) {
					return;
				}
				setRuntimeDisconnected(true);
				log.error("WebSocket connection failed");
				dispatch({
					type: "stream_disconnected",
					message: "Runtime stream connection failed.",
				});
			};
		};

		connect();

		return () => {
			cancelled = true;
			if (reconnectTimer != null) {
				window.clearTimeout(reconnectTimer);
			}
			cleanupSocket();
		};
	}, [requestedWorkspaceId]);

	return {
		currentProjectId: state.currentProjectId,
		projects: state.projects,
		workspaceState: state.workspaceState,
		workspaceMetadata: state.workspaceMetadata,
		notificationSessions: state.notificationSessions,
		notificationWorkspaceIds: state.notificationWorkspaceIds,
		latestTaskReadyForReview: state.latestTaskReadyForReview,
		latestTaskTitleUpdate: state.latestTaskTitleUpdate,
		debugLoggingEnabled: state.debugLoggingEnabled,
		debugLogEntries: state.debugLogEntries,
		streamError: state.streamError,
		isRuntimeDisconnected: state.isRuntimeDisconnected,
		hasReceivedSnapshot: state.hasReceivedSnapshot,
	};
}
